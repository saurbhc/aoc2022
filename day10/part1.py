def compute(s: str) -> int:
    # CPU Register
    X = 1

    # Cycle Count
    cycle = 0

    # Signal Strength
    signal_strengths = []

    lines = s.splitlines()
    for line in lines:
        if line.split(' ')[0] == 'addx':
            register = int(line.split(' ')[1])
            _cycle = 2
            for _ in range(_cycle):
                cycle += 1
                if cycle in list(range(20, 221, 40)):
                    _signal_strength = cycle * X
                    signal_strengths.append(_signal_strength)

            # Increase register
            X += register
        else:
            _cycle = 1
            cycle += 1

            if cycle in list(range(20, 221, 40)):
                _signal_strength = cycle * X
                signal_strengths.append(_signal_strength)

    return sum(signal_strengths)


