INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def compute(s: str) -> str:
    # CPU Register
    X = 1

    # CRT
    CRT_rows = []
    CRT = ''

    # Cycle Count
    cycle = 0

    lines = s.splitlines()
    for line in lines:
        if line.split(' ')[0] == 'addx':
            register = int(line.split(' ')[1])
            _cycle = 2
            for _ in range(_cycle):
                cycle += 1

                if cycle in range(X, X+3):
                    CRT += '#'
                else:
                    CRT += '.'
                # print(len(CRT), cycle, X, line)
                if len(CRT) in list(range(40, 241, 40)):
                    CRT_rows.append(CRT)
                    CRT = ''
                    cycle = 0

            # Increase register
            X += register
        else:
            _cycle = 1
            cycle += 1

            if cycle in range(X, X+3):
                CRT += '#'
            else:
                CRT += '.'
            if len(CRT) in list(range(40, 241, 40)):
                CRT_rows.append(CRT)
                CRT = ''
                cycle = 0
    a = r''
    for i in CRT_rows:
        a += ''.join(i) + '\n'
    # print(a)

    return a


